package clase06;

public class StringSubstring {
    public static void main(String[] args) {
        String s1 = "I eat apples";
        String s2 = "Bananas are my favorite fruit";
        System.out.println(s1.substring(6)+" "+s2.substring(8,23));
        //Substring puede llevar solo el indice de inicio o llevar indice de inicio con su indice de final (tipo rango)

    }
}
